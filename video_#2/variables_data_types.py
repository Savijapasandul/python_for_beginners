# Creating and using variables
name = "Alice"        # String variable
age = 30              # Integer variable
height = 5.5          # Float variable

# Accessing variables
print("Name:", name)  # Outputs: Name: Alice
print("Age:", age)    # Outputs: Age: 30
print("Height:", height)  # Outputs: Height: 5.5

# Modifying variables
age = 31              # Change the value of age
print("New Age:", age)  # Outputs: New Age: 31

# Changing type of variable
age = "thirty-one"    # Now age is a string
print("Age as text:", age)  # Outputs: Age as text: thirty-one

### In this example:
# We create and initialize variables with different types of data.
# We access and print the values of these variables.
# We modify the value of `age`.
# We change the type of `age` from an integer to a string, demonstrating Python's dynamic typing.
